package com.xmjd.info.commons.exception;

public class ParamException extends CiBizException {

    public ParamException() {
        super();
    }

    public ParamException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ParamException(String arg0) {
        super(arg0);
    }

    public ParamException(Throwable arg0) {
        super(arg0);
    }

}
